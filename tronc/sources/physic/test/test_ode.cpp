      void TestODE::testTrimesh()
      {
        InternalMessage("Physic","Physic::Test::TestODE::testTrimesh Entering") ;
        dInitODE();

        std::auto_ptr<dWorld> world(new dWorld()) ;
        std::auto_ptr<dBody> body(new dBody(world->id())) ;

        Size[0] = 5.0f;
        Size[1] = 5.0f;
        Size[2] = 2.5f;

        Vertices[0][0] = -Size[0];
        Vertices[0][1] = -Size[1];
        Vertices[0][2] = Size[2];

        Vertices[1][0] = Size[0];
        Vertices[1][1] = -Size[1];
        Vertices[1][2] = Size[2];

        Vertices[2][0] = Size[0];
        Vertices[2][1] = Size[1];
        Vertices[2][2] = Size[2];

        Vertices[3][0] = -Size[0];
        Vertices[3][1] = Size[1];
        Vertices[3][2] = Size[2];

        Vertices[4][0] = 0;
        Vertices[4][1] = 0;
        Vertices[4][2] = 0;

        Indices[0] = 0;
        Indices[1] = 1;
        Indices[2] = 4;

        Indices[3] = 1;
        Indices[4] = 2;
        Indices[5] = 4;

        Indices[6] = 2;
        Indices[7] = 3;
        Indices[8] = 4;

        Indices[9] = 3;
        Indices[10] = 0;
        Indices[11] = 4;

        dTriMeshDataID Data = dGeomTriMeshDataCreate();

        dGeomTriMeshDataBuildSingle(Data,
                                    &Vertices[0],3*sizeof(float),VertexCount,
                                    &Indices[0],IndexCount,3*sizeof(int));

        TriMesh = dCreateTriMesh(0, Data, 0, 0, 0) ;
        dGeomSetData(TriMesh, Data) ;

        dMass mass ;
        mass.setSphere(1,1) ;
        mass.adjust(1) ;
        body->setMass(&mass) ;

        dGeomSetBody(TriMesh,body->id()) ;

        body->setPosition(100,100,100) ;

        dReal aabb[6] ;
        dGeomGetAABB(TriMesh,aabb) ;

        // Destroy the mesh
        dGeomTriMeshClearTCCache(TriMesh) ;
        dGeomTriMeshDataDestroy(Data) ;
        dGeomDestroy(TriMesh) ;

        dCloseODE() ;
      }

